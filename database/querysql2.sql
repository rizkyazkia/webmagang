
CREATE TABLE logistik_ruangan(
    Kode_Ruangan VARCHAR(25) PRIMARY KEY,
    Lokasi_Ruangan VARCHAR(255),
    Gedung_Ruangan VARCHAR(255),
    Nama_Ruangan VARCHAR(255),
    Lantai_Ruangan INT(2));

CREATE TABLE logistik_documents(
    Kode_Document INT AUTO_INCREMENT PRIMARY KEY,
    Kode_Ruangan VARCHAR(255),
    Periode_Document TEXT,
    CreatedAt DATETIME DEFAULT CURRENT_TIME,
    FOREIGN KEY (Kode_Ruangan) REFERENCES logistik_ruangan(Kode_Ruangan) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE logistik_assets(
	ID_LogAssets INT AUTO_INCREMENT PRIMARY KEY,
    Kode_Barang VARCHAR(255),
    Kode_Document INT,
    Nama_Barang VARCHAR(255),
    Jumlah_Barang INT(5),
    Satuan_Barang VARCHAR(50),
    Ket_Barang VARCHAR(255),
    Pengecekan_Barang VARCHAR(255),
    FOREIGN KEY (Kode_Document) REFERENCES logistik_documents(Kode_Document) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE logistik_assets_directory(
	ID_Assets INT PRIMARY KEY AUTO_INCREMENT,
	Nama_Assets VARCHAR(200)
);

CREATE OR REPLACE PROCEDURE GetKodeRuangan(
	IN KodeRuangan TEXT,
	IN Periode TEXT
)
BEGIN
	INSERT INTO logistik_documents VALUES("",KodeRuangan,Periode,CURRENT_TIMESTAMP);
END;

CREATE OR REPLACE PROCEDURE InsertAssets(
	IN Kode_Barang TEXT,
	IN Nama_Barang TEXT,
	IN Jumlah_Barang TEXT,
	IN Satuan_Barang TEXT,
	IN Ket_Barang TEXT,
	IN Check_Barang TEXT
)
BEGIN
	DECLARE KDDoc INT;
	SELECT Kode_Document FROM logistik_documents ORDER BY `Kode_Document` DESC LIMIT 1 INTO KDDoc;
	INSERT INTO logistik_assets VALUES("",Kode_Barang,KDDoc,Nama_Barang,Jumlah_Barang,Satuan_Barang,Ket_Barang,Check_Barang);
END;

